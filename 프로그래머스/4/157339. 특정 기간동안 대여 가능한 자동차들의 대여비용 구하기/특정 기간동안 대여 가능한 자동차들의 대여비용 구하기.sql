SELECT
    C.CAR_ID,
    C.CAR_TYPE,
    ROUND(C.DAILY_FEE * 30 * (100 - D.DISCOUNT_RATE) / 100) AS FEE
FROM CAR_RENTAL_COMPANY_CAR AS C
JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN AS D
  ON C.CAR_TYPE = D.CAR_TYPE
  AND D.DURATION_TYPE = '30일 이상'
WHERE
    -- 1) 차종
    C.CAR_TYPE IN ('세단', 'SUV')
    -- 2) 11월(11/1~11/30)과 겹치는 대여 기록이 하나도 없어야 한다
    AND C.CAR_ID NOT IN (
        SELECT CAR_ID
        FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
        WHERE
              (START_DATE BETWEEN '2022-11-01' AND '2022-11-30')
           OR (END_DATE   BETWEEN '2022-11-01' AND '2022-11-30')
           OR (START_DATE < '2022-11-01' AND END_DATE > '2022-11-30')
    )
    -- 3) 요금 범위
    AND ROUND(C.DAILY_FEE * 30 * (100 - D.DISCOUNT_RATE) / 100) >= 500000
    AND ROUND(C.DAILY_FEE * 30 * (100 - D.DISCOUNT_RATE) / 100) < 2000000
ORDER BY
    FEE DESC,
    C.CAR_TYPE ASC,
    C.CAR_ID DESC;
